package Empresa;

public class Dependentes {

    
    // ATRIBUTOS DA CLASSE DEPENDENTE
    
    String nome;
    int idade;
    String CPF;

   
    // GETTERS E SETTERS DA CLASSE DEPENDENTE
    
    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public int getIdade() {
        return idade;
    }

    public void setIdade(int idade) {
        this.idade = idade;
    }
    
    public String getCPF(){
        return CPF;
    }
    
    public void setCPF (String CPF){
        this.CPF = CPF;
    }

}
